    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {   
        $order_statuses = OrderStatus::get();
        $vendor = Vendor::all()->first();
        $order_query = $vendor->orders();
        if($request->has('status')){
            $order_query->whereHas('order', function($q) use($request) {
                $q->where('status', $request->status);
            });
        }
        $orders = $order_query->orderBy('created_at','desc')->paginate(10);
        $orders->load('order','product');
        return view('supplier.orders.index',compact('orders','order_statuses'));
    }

